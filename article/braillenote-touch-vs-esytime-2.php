<h3>Introduction</h3>

<p>  Recently, I started looking to get my old, tatty braillenote apex bt32 notetaker replaced with something new. I, along with my VI team at school found 2 devices. The braillenote touch, which is a braille tablet running android and with new features including touch braille (where you can type in braille on the glass of the screen) and the esytime, a braille laptop that runs full blown windows 7 (soon to be windows 10) with some cool features as well. I then published the first report on these devices, documenting all of my research up until that point. After that, I got somebody from each side of the battle to come out and demonstrate there product. This, my second report, is the most important notes <span style="font-style: normal">I found out from these people and further research.</span></p>

<p><b>I'll also publish a list of questions I had for theaformentioned sails people later, so you can see what my thoughtprocess was.</b></p>

<p><b>Also note that this document was written for me and the VI team to decide which device to buy, if any and was not originally meant to be published here.</b></p>

