<h2>The Blind Installer Project</h2>
<p>The blind installer project (bip) is a project started by Michael Buchan to give new blind computer users an easy way to access their computer. For example, a new blind user may download this software. The software will then install a screen reader, any screen reader add-ons, a magnifier and any other accessible, programs and utilities, all depending on the user's selections. It will then walk the user through how to use the software, as well as some of the essential preferences  toolsfor each piece of software, setting them automatically.</p>
<h3>how to contribute</h3>
<p>The code for this project is up on github. You can <a href="https://github.com/mcb2003/blind-installer">visit the github page</a> if you would like to contribute.</p>
<p>Right now, this software is in the concept stage and we're still trying to figure out what we want to implement and how we want to implement it. If you would like to help with this, please see the <a href="https://github.com/mcb2003/blind-installer/issues">Github issues page</a>.</p>
