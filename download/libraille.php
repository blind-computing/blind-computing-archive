<h2>The LiBraille Project</h2>
<p>  LiBraille is a tool designed to be used by braille transcribers and similar people who transcribe print formatted text of any format into electronic braille to be embossed or viewed on a refreshable braille display by the blind or visually impaired. Many products already exist in this market, such as duxbury systems' Duxbury transcription software and braille translation facilities, however, to my knowledge, LiBraille is the only fully open source, extensible, community driven equivilant.
<p>  You can find the code for LiBraille at <a href="https://github.com/mcb2003/LiBraille" target="about:blank" title="Links to the GitHub page for LiBraille. Opens in a new tab">the Github repository for LiBraille</a> and help us out with bug reports and feature requests by <a href="https://github.com/mcb2003/LiBraille/issues" target="about:blank" title="Links to the Github issues page for LiBraille. Opens in a new tab">submitting an issue</a>.</p>
